// in this lesson in freeCodeCamp we were instructed to rearrange an if, else if, else statement so the logic operators would properly work.
function orderMyLogic(val) {
  if (val < 5) {
    return "Less than 5";
  } else if (val < 10) {
    return "Less than 10";
  } else {
    return "Greater than or equal to 10";
  }
}

// Change this value to test
orderMyLogic(7);
