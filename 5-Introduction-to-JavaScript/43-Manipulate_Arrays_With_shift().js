// in this lesson I learned about the shift function. It is very similar to the pop function except it will remove from the front of the array instead of the back
// Example
var ourArray = ["Stimpson", "J", ["cat"]];
var removedFromOurArray = ourArray.shift();
// removedFromOurArray now equals "Stimpson" and ourArray now equals ["J", ["cat"]].

